"""
Section 3.2 — Stacking Ensemble with Bayesian Optimization (Optuna-TPE)
Fully aligned, runnable, and reviewer-friendly implementation.

What this script does (aligned with the Methods text):
1) Builds a stacking ensemble with three heterogeneous base learners:
   - XGBoost (tree boosting)
   - Random Forest (bagging of decision trees)
   - Multilayer Perceptron (neural network)
   and a Logistic Regression meta-learner.

2) Uses ONLY base-learner probability outputs as meta-learner inputs:
   - passthrough = False
   - stack_method = 'predict_proba'
   This matches: "probability outputs generated by each base learner were used
   as inputs to the meta-learner".

3) Jointly tunes hyperparameters of all learners via Bayesian search:
   - Optuna TPESampler (TPE is widely used as a Bayesian/TPE-based optimizer)
   - Objective: maximize 5-fold stratified cross-validation ROC-AUC
   - Includes pruning to speed up search

4) Adds six multiplicative interaction terms (exact combinations stated in Methods):
   slope–rainfall, lithology–fault distance, slope–river distance,
   NDVI–rainfall, slope–structure, and TWI–rainfall.

5) Prevents data leakage:
   - Train/test split first
   - StandardScaler is fit on TRAIN only, then applied to TEST

6) Exports multi-angle feature importance consistent with the Methods:
   - XGBoost gain importance
   - Random Forest MDI importance
   - MLP permutation importance (scoring = ROC-AUC)
   - Standardized LR coefficients (for meta-features, since passthrough=False)

Outputs (saved under CFG['out_dir']):
- best_params.json
- cv_auc.txt
- test_metrics.json
- xgb_gain.csv
- rf_mdi.csv
- mlp_permutation_auc.csv
- meta_lr_stdcoef.csv
- scaler.pkl
- stacking_model.pkl
"""

